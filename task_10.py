a = int(input("Введите целое число a = "))
b = int(input("Введите целое число b = "))

print("Сумма чисел a и b = ", a+b)
print("Разность чисел a - b", a-b)
print("Произвидение чисел a и b = ", a*b)
print("Частное чисел a и b = ", a//b)
print("Остаток от деление a и b = ", a%b)
print("Результат возведение числа a в степень b = ", a**b)